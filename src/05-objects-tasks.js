/**
 * Returns the rectangle object with width and height parameters and getArea() method
 *
 * @param {number} width
 * @param {number} height
 * @return {Object}
 *
 * @example
 *    const r = new Rectangle(10,20);
 *    console.log(r.width);       // => 10
 *    console.log(r.height);      // => 20
 *    console.log(r.getArea());   // => 200
 */
// eslint-disable-next-line max-classes-per-file
function Rectangle(width, height) {
  // Create class
  class RectangleES6 {
    // Constructor for fields
    constructor(widthR, heightR) {
      this.width = widthR;
      this.height = heightR;
    }

    // Method for calculate area
    getArea() {
      return this.width * this.height;
    }
  }

  // Return object from class
  return new RectangleES6(width, height);
}


/**
 * Returns the JSON representation of specified object
 *
 * @param {object} obj
 * @return {string}
 *
 * @example
 *    [1,2,3]   =>  '[1,2,3]'
 *    { width: 10, height : 20 } => '{"height":10,"width":20}'
 */
function getJSON(obj) {
  return JSON.stringify(obj);
}


/**
 * Returns the object of specified type from JSON representation
 *
 * @param {Object} proto
 * @param {string} json
 * @return {object}
 *
 * @example
 *    const r = fromJSON(Circle.prototype, '{"radius":10}');
 *
 */
function fromJSON(proto, json) {
  // Parse source object from JSON
  const object = JSON.parse(json);
  // Set prototype of parsed object from proto arg, using Object.setPrototypeOf(object, proto);
  Object.setPrototypeOf(object, proto);
  return object;
}


